const slug = new URLSearchParams(window.location.search).get('slug');
const page = detailPages[slug] || detailPages['not-found'];

const setText = (id, value) => {
  const el = document.getElementById(id);
  if (el) el.textContent = value;
};

document.title = `${page.title} | Icebrothes®`;

setText('detail-eyebrow', page.eyebrow);
setText('detail-title', page.title);
setText('detail-lead', page.lead);
setText('detail-caption', page.caption);

const backLink = document.getElementById('detail-back-link');
if (backLink) {
  backLink.href = page.backHref || 'index.html';
}

const titleLogo = document.getElementById('detail-title-logo');
if (titleLogo) {
  const hasTitleLogo = Boolean(page.titleLogo);
  titleLogo.hidden = !hasTitleLogo;

  if (hasTitleLogo) {
    titleLogo.src = page.titleLogo;
    titleLogo.alt = page.titleLogoAlt || '';
  }
}

const image = document.getElementById('detail-image');
const imageFrame = image ? image.closest('.page-visual') : null;
if (image) {
  image.src = page.image;
  image.alt = page.alt;
}

if (imageFrame) {
  imageFrame.classList.toggle('page-visual-contain', page.imageFit === 'contain');
}

const facts = document.getElementById('detail-facts');
if (facts) {
  facts.innerHTML = page.facts
    .map((fact) => `<li>${fact}</li>`)
    .join('');
}

const body = document.getElementById('detail-body');
if (body) {
  body.innerHTML = page.body
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join('');
}

const gallerySection = document.getElementById('detail-gallery-section');
const galleryTitle = document.getElementById('detail-gallery-title');
const gallery = document.getElementById('detail-gallery');

if (gallerySection && gallery) {
  const hasGallery = Array.isArray(page.gallery) && page.gallery.length > 0;
  const isEventPage = typeof slug === 'string' && slug.startsWith('event-');
  const isLogoPage = typeof slug === 'string' && slug.startsWith('logo-');
  gallerySection.hidden = !hasGallery;
  gallerySection.classList.toggle('event-gallery', hasGallery && isEventPage);
  gallerySection.classList.toggle('logo-gallery', hasGallery && isLogoPage);

  if (hasGallery) {
    if (galleryTitle && page.galleryTitle) {
      galleryTitle.textContent = page.galleryTitle;
    }

    gallery.innerHTML = page.gallery
      .map(
        (item) => `
          <figure class="card detail-gallery-item${item.featured ? ' detail-gallery-item-featured' : ''}">
            <img src="${item.src}" alt="${item.alt}"${item.objectPosition ? ` style="object-position:${item.objectPosition};"` : ''}>
            <figcaption>${item.caption}</figcaption>
          </figure>
        `
      )
      .join('');
  }
}

const related = document.getElementById('detail-related');
if (related) {
  related.innerHTML = page.related
    .map(
      (item) => `
        <a class="related-link" href="detail.html?slug=${item.slug}">
          <span class="related-link-eyebrow">${item.eyebrow}</span>
          <span>${item.title}</span>
        </a>
      `
    )
    .join('');
}
